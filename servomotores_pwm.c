#define SERVO_CERRADO 3000u
#define SERVO_ABIERTO 6600u

//funcion para mover servos
void persiana_set_pct(uint8_t persiana, uint8_t pct)
{
  if (pct > 100)
	  pct = 100;

  uint32_t valor = SERVO_CERRADO
               + ((uint32_t)pct * (SERVO_ABIERTO - SERVO_CERRADO)) / 100u;

  if (persiana == 1) {
    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, valor);
  } else if (persiana == 2) {
    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, valor);
  }
}




